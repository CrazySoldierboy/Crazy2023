public class Round {
    public static void main(String[] args) {
        float num = 7.25f;
        System.out.println(num + " Округленное равно " + Math.round(num));
        System.out.println(num + " Округленное равно " + Math.floor(num));
        System.out.println(num + " Округленное равно " + Math.ceil(num));
    }
}
